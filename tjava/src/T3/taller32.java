package T3;

public class taller32 {




    public static void main(String[]args){
        int i=10;
        int k=1;
        int l=-1;

        while (0<=i){
            while (k<=i) {
                System.out.print(" ");
                k++;





                }
            while (l<=10-k){
                System.out.print("*");
                l++;
            }
            l=-1;
            System.out.println("");
            i--;
            k=1;
            }

    }}




